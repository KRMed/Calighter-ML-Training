def tag_entity(tokens, entity, prefix_list, label):
    if not entity:
        return [None] * len(tokens)

    # Tokenize and clean entity
    entity_tokens = word_tokenize(entity)
    entity_tokens_clean = [t for t in entity_tokens if t not in QUOTES]
    entity_lc = [t.lower() for t in entity_tokens_clean]

    # Remove prefix if it matches
    prefix_len = 0
    for prefix in sorted(prefix_list, key=lambda x: -len(word_tokenize(x))):
        prefix_tokens = word_tokenize(prefix.lower())
        if entity_lc[:len(prefix_tokens)] == prefix_tokens:
            prefix_len = len(prefix_tokens)
            break

    # Create tag sequence (with no quotes or prefix)
    tag_seq = []
    for i, tok in enumerate(entity_tokens_clean):
        if i < prefix_len:
            tag_seq.append(None)  # skip wrapper prefix
        else:
            tag_seq.append(f'B-{label}' if i == prefix_len else f'I-{label}')

    # Initialize tag list for sentence tokens
    sent_tags = [None] * len(tokens)

    # Align entity tokens with sentence tokens
    ent_idx = 0
    for i, tok in enumerate(tokens):
        tok_clean = re.sub(r"[^\w]+", "", tok.lower())
        ent_tok = entity_tokens_clean[ent_idx] if ent_idx < len(entity_tokens_clean) else None
        ent_tok_clean = re.sub(r"[^\w]+", "", ent_tok.lower()) if ent_tok else None

        if tok in QUOTES or re.fullmatch(r"[^\w]+", tok):
            continue  # skip quotes/punctuation

        if ent_tok_clean and tok_clean == ent_tok_clean:
            tag = tag_seq[ent_idx]
            if tag is not None:
                sent_tags[i] = tag
            ent_idx += 1

        if ent_idx >= len(entity_tokens_clean):
            break

    return sent_tags

def tag_time_entity(tokens, time_phrase, prefix_list):
    if not time_phrase:
        return [None] * len(tokens)

    time_tokens = word_tokenize(time_phrase)
    time_lc = [t.lower() for t in time_tokens]

    # Match full wrapper prefixes (e.g., "all day", "this year") as sequences
    sorted_prefixes = sorted(prefix_list, key=lambda x: -len(word_tokenize(x)))
    prefix_len = 0
    for prefix in sorted_prefixes:
        prefix_tokens = word_tokenize(prefix.lower())
        if time_lc[:len(prefix_tokens)] == prefix_tokens:
            prefix_len = len(prefix_tokens)
            break

    # Build the tag sequence for time_phrase
    tag_seq = []
    for i in range(len(time_tokens)):
        if i < prefix_len:
            tag_seq.append(None)  # Wrapper part — no tag
        else:
            tag_seq.append("B-TIME" if i == prefix_len else "I-TIME")

    # Align tags with sentence tokens
    sent_tags = [None] * len(tokens)
    ent_idx = 0
    tok_idx = 0
    while ent_idx < len(time_lc) and tok_idx < len(tokens):
        tok = tokens[tok_idx]
        if tok in QUOTES or re.fullmatch(r"[^\w]+", tok):
            tok_idx += 1
            continue
        if tok.lower() == time_lc[ent_idx]:
            if tag_seq[ent_idx] is not None:
                sent_tags[tok_idx] = tag_seq[ent_idx]
            ent_idx += 1
        tok_idx += 1

    return sent_tags

def split_time_tokens(time_tokens):
    if len(time_tokens) >= 4 and time_tokens[0].lower() in ("between", "from"):
        try:
            join_idx = time_tokens.index("and")
        except ValueError:
            try:
                join_idx = time_tokens.index("to")
            except ValueError:
                join_idx = -1
        if join_idx > 0 and join_idx < len(time_tokens) - 1:
            prefix = [time_tokens[0]]
            t1 = time_tokens[1:join_idx]
            join = [time_tokens[join_idx]]
            t2 = time_tokens[join_idx+1:]
            return prefix, t1, join, t2
    return [], time_tokens, [], []

